package Pranavi;

public class StringOperations {

	public static void main(String[] args) {
		String str1="Pranavi";
		System.out.println(str1.equalsIgnoreCase("pranavi"));
		System.out.println("length "+str1.length());
		System.out.println(str1.lastIndexOf("a"));
		System.out.println(str1.concat(" Ryakala"));
		System.out.println(str1.indexOf("i"));
		System.out.println(str1.contains("navi"));
		System.out.println(str1.isEmpty());
		System.out.println(str1.endsWith("i"));
		System.out.println(str1.compareTo("Pranavi Ryakala"));
		System.out.println(str1.compareTo("Pranavi"));
		System.out.println(str1.compareTo("pranavi"));
		System.out.println(str1.charAt(0));
		 
			 
	}

}
